        <script>
          const quizData = [{
              question: "Penyakit yang menyebabkan gangguan fungsi sendi atau peradangan sendi dengan gejala bengkak, rasa sakit, dan kemerahan disebut...",
              answers: {
                a: "Osteoporosis",
                b: "Arthritis",
                c: "Scoliosis",
                d: "Tendinitis"
              },
              correct: "b",
              explanation: "Arthritis adalah penyakit yang menyebabkan peradangan pada sendi, yang ditandai dengan gejala seperti bengkak, rasa sakit, dan kemerahan pada sendi"
            },
            {
              question: "Kelainan pada tulang belakang bagian Bawah yang melengkung terlalu rendah menyebabkan sakit dan ketidaknyamanan disebut...",
              answers: {
                a: "Kifosis",
                b: "Skoliosis",
                c: "Lordosis",
                d: "Osteoporosis"
              },
              correct: "c",
              explanation: "Lordosis adalah kelainan pada tulang belakang bagian bawah yang ditandai dengan lengkungan yang berlebihan ke arah dalam (menjauh dari garis tengah tubuh), yang dapat menyebabkan sakit dan ketidaknyamanan pada bagian tulang belakang tersebut."
            }
          ];

          const quizContainer = document.getElementById('quiz');
          const resultsContainer = document.getElementById('results');
          const nextButton = document.getElementById('next');
          const submitButton = document.getElementById('submit');
          const backButton = document.getElementById('back-to-main');
          const scoreContainer = document.getElementById('score');
          let currentQuestionIndex = 0;
          let score = 0;

          function buildQuiz() {
            const currentQuestion = quizData[currentQuestionIndex];
            const answers = [];

            for (let letter in currentQuestion.answers) {
              answers.push(
                `<label class="option">
                            <input type="radio" name="question${currentQuestionIndex}" value="${letter}">
                            ${currentQuestion.answers[letter]}
                        </label>`
              );
            }

            const output =
              `<div class="question">${currentQuestion.question}</div>
                    <div class="answers">${answers.join('')}</div>`;

            quizContainer.innerHTML = output;
          }

          function submitQuiz() {
            const currentQuestion = quizData[currentQuestionIndex];
            const answerContainer = quizContainer.querySelector('.answers');
            const selector = `input[name=question${currentQuestionIndex}]:checked`;
            const userAnswer = (answerContainer.querySelector(selector) || {}).value;
            const correctAnswer = currentQuestion.correct;
            const explanation = currentQuestion.explanation;

            let feedback = '';
            if (userAnswer === correctAnswer) {
              answerContainer.querySelector(`input[value=${userAnswer}]`).parentElement.classList.add('correct');
              score++;
              feedback = '<p class="feedback correct-feedback">Jawaban kamu benar.</p>';
            } else {
              if (userAnswer) {
                answerContainer.querySelector(`input[value=${userAnswer}]`).parentElement.classList.add('incorrect');
                feedback = '<p class="feedback incorrect-feedback">Jawaban kamu salah.</p>';
              }
              answerContainer.querySelector(`input[value=${correctAnswer}]`).parentElement.classList.add('correct');
            }

            resultsContainer.innerHTML = feedback + explanation;

            if (currentQuestionIndex < quizData.length - 1) {
              nextButton.style.display = 'block';
              submitButton.style.display = 'none';
            } else {
              submitButton.style.display = 'none';
              nextButton.style.display = 'none';
              backButton.style.display = 'block';
              showScore();
            }
          }

          function nextQuestion() {
            currentQuestionIndex++;
            resultsContainer.innerHTML = '';
            nextButton.style.display = 'none';
            submitButton.style.display = 'block';
            buildQuiz();
          }

          function showScore() {
            scoreContainer.innerHTML = `Skor kamu adalah ${score} dari ${quizData.length}`;
            scoreContainer.style.display = 'block';
          }

          // Fungsi untuk kembali ke menu utama
          function backToMain() {
            window.location.href = 'Pilhan-Materi.html'; // Ganti 'index.html' dengan halaman utama Anda
          }

          buildQuiz();
        </script>
